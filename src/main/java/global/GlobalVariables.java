package global;

/**
 * <p>
 * Contém variáveis que podem ser usadas globalmente no projeto
 * </p>
 */
public class GlobalVariables {

    /**
     * <p>
     * Indica se pode-se ler o conteúdo de dentro das chaves de um if
     * </p>
     */
    public static boolean LER_CHAVES;

    /**
     * <p>
     * Indica se um if foi avaliado e que as próximas linhas só devam ser
     * interpretadas de acordo com a avaliação da condicional
     * </p>
     */
    public static boolean ENCONTROU_IF;

}
